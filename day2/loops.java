package day2;

public class loops {
    public static void main(String[] args){
        //for loop
        for(int i=0;i<6;i++){
            System.out.println(i + " ");
        }

        //while loop
        int j=1;
        while(j<6){
            System.out.println(j + " ");
            j++;
        }

        //do while loop 
        int k =10;
        do{
            System.out.println(k + " ");
            k++;
        }while (k<= 5);

    }
    
}
